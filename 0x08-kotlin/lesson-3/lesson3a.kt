// For loop control structure
fun main(){
    
    // list of counties
    val counties = arrayOf("Nairobi", "Kisumu", "Makueni", "Mombasa", "Kilifi", "Kwale", "Machakos")

    // loop through the list
    for (county in counties){
        println("$county")
    }

    // list of animals in zoo
    val animals = arrayOf("Lion", "Snake", "Elephant", "Tiger")

    // loop through the list
    for (animal in animals){
        println("$animal")
    }

    // items in shopping cart
    val items = arrayOf("Milk", "Sugar", "Cooking Oil", "Shoe Polish", "Drinking Water")

    // loop through the list
    for(item in items){
        println("$item")
    }

    // list of movies
    val movies = arrayOf("Last King of Scotland", "Pride and Prejidence", "Red Notice", "Expendables")

    // loop through the list
    for(movie in movies){
        println("$movie")
    }
}