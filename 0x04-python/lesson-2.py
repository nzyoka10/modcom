# Arithmetic Operators
number1 = 40
number2 = 70

# sum
sum = number1 + number2
print("Sum is: ", sum)

# Numbers
num1 = 14
num2 = 7

# division
division = num1 / num2
print("Division: ", division)

# Subtraction
sub = num1 - num2
print("Subtraction: ", sub)

# Multiplication
multiplication = num1 * num2
print("Multiplication: ", multiplication)

# Modulus
modulus = num2 % num1
print("Modulus: ", modulus)

# Exponentiation
expo = num1 ** num2
print("Exponentiation: ", expo)

# Floor division
floor_div = num1 // num2
print("Floor division: ", floor_div) 