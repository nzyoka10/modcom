
# The if-else Statement:-
# It works for both True and False statement
    # Executes one block of code if the condition is true, and another 
    # block if it is false.
print("\tThe if-else Statement")

# Example 1
temperature = 10
if temperature > 25:
    print("Hot day")
else:
    print("Cold day!")

# Example 2
number = -10
if number >= 0:
    print("Positive number")
else:
    print("Negative number")

# Example 3
gender = "Male"
if gender == "Male":
    print("Male")
else:
    print("Female")

# Check gender using Boolean
# True indicates Male, False indicates Female
is_male = False

if is_male:
    print("Male")
else:
    print("Female")
